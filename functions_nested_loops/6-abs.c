/**
 * _abs - calcul la valeur absolue d'un entier
 * @j: permet de donner d'une valeur absolue d'un nombre
 * Return: si c'est négative ou égal à zero (0) sinon (1)
 *
 */
int _abs(int j)
{
	if (j < 0)
	{
		return (j);
	}
	else if (j >= 0)
	{
		return (j);
	}
	return (0);
}
